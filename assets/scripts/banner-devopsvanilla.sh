# Print the banner and color all occurrences of the character "짹" in orange.
# Uses ANSI 256-color (38;5;208). Falls back to no color if stdout is not a TTY
# or if NO_COLOR is set.

# Determine color sequences (allows forcing via CLICOLOR_FORCE=1 or FORCE_COLOR=1)
if { [ -t 1 ] || [ "${CLICOLOR_FORCE:-}" = "1" ] || [ "${FORCE_COLOR:-}" = "1" ]; } \
	&& [ -z "${NO_COLOR:-}" ]; then
	ORANGE=$'\033[38;5;208m'
	RESET=$'\033[0m'
else
	ORANGE=''
	RESET=''
fi

# Emit the banner; substitute 짹 with orange 짹
sed "s/짹/${ORANGE}짹${RESET}/g" <<'EOF'
